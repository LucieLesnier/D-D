package donjon;

public class Weaponry {

	private int name;
	private int power;

	public Weaponry(int name, int power) {
		super();
		this.name = name;
	}

	public int getName() {
		return name;
	}

	public void setName(int name) {
		this.name = name;
	}

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

}
